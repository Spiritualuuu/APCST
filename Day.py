inputOne = int(input())

if(inputOne == 1):
    print("Sunday")
elif(inputOne == 2):
    print("Monday")
elif(inputOne == 3):
    print("Tuesday")
elif(inputOne == 4):
    print("Wednesday")
elif(inputOne == 5):
    print("Thursday")
elif(inputOne == 6):
    print("Friday")
else:
    print("Saturday")